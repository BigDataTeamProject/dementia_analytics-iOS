import UIKit

extension UIFont {
    static func kavoon(_ size: CGFloat) -> UIFont{
        return UIFont.init(name: "Kavoon-Regular", size: size)!
    }
    static func bmEuljiro(_ size: CGFloat) -> UIFont{
        return UIFont.init(name: "BMEULJIROTTF", size: size)!
    }
}

